public class Punto4 {
public static void main(String[] args) {
char categoria = 'a';
switch (categoria) {
case 'a':
System.out.println("hijo");
break;
case 'b':
System.out.println("padres");
break;
case 'c':
System.out.println("abuelos");
break;
default:
System.out.println("Categoría inválida");
}
}
}